package com.lio.markit.beans;

public class StockDTO {

	private String Open; 
	private String Low; 
	private String Change; 
	private String Volumne; 
	private String Timestamp; 
	private String Name; 
	private String ChangePercent; 
	private String Status; 
	private String Symbol; 
	private String LastPrice; 
	private String MSDate; 
	private String ChangeYTD; 
	private String MarketCap; 
	private String ChangePercentYTD; 
	private String High;
	public String getOpen() {
		return Open;
	}
	public void setOpen(String open) {
		Open = open;
	}
	public String getLow() {
		return Low;
	}
	public void setLow(String low) {
		Low = low;
	}
	public String getChange() {
		return Change;
	}
	public void setChange(String change) {
		Change = change;
	}
	public String getVolumne() {
		return Volumne;
	}
	public void setVolumne(String volumne) {
		Volumne = volumne;
	}
	public String getTimestamp() {
		return Timestamp;
	}
	public void setTimestamp(String timestamp) {
		Timestamp = timestamp;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getChangePercent() {
		return ChangePercent;
	}
	public void setChangePercent(String changePercent) {
		ChangePercent = changePercent;
	}
	public String getStatus() {
		return Status;
	}
	public void setStatus(String status) {
		Status = status;
	}
	public String getSymbol() {
		return Symbol;
	}
	public void setSymbol(String symbol) {
		Symbol = symbol;
	}
	public String getLastPrice() {
		return LastPrice;
	}
	public void setLastPrice(String lastPrice) {
		LastPrice = lastPrice;
	}
	public String getMSDate() {
		return MSDate;
	}
	public void setMSDate(String mSDate) {
		MSDate = mSDate;
	}
	public String getChangeYTD() {
		return ChangeYTD;
	}
	public void setChangeYTD(String changeYTD) {
		ChangeYTD = changeYTD;
	}
	public String getMarketCap() {
		return MarketCap;
	}
	public void setMarketCap(String marketCap) {
		MarketCap = marketCap;
	}
	public String getChangePercentYTD() {
		return ChangePercentYTD;
	}
	public void setChangePercentYTD(String changePercentYTD) {
		ChangePercentYTD = changePercentYTD;
	}
	public String getHigh() {
		return High;
	}
	public void setHigh(String high) {
		High = high;
	}
	
}
